/**
 * measure-coverage — honest per-file coverage table, generated from the vitest
 * v8 run (never hand-typed, so the published numbers can't drift from reality).
 *
 * Reads `coverage/coverage-summary.json` (the `json-summary` reporter, enabled
 * in vitest.config.ts). Run `npm run test:coverage` first, or use `coverage:doc`
 * which chains both. `include`/`exclude` are decided by vitest.config.ts — this
 * script only formats whatever the summary contains.
 *
 * Run: node scripts/measure-coverage.mjs            human table (default)
 *      node scripts/measure-coverage.mjs --json      machine-readable
 *      node scripts/measure-coverage.mjs --md         full generated doc (docs/COVERAGE.md)
 *
 * `npm run coverage:doc` writes --md to docs/COVERAGE.md; wire a CI
 * `git diff --exit-code` on it (like size:doc) so the numbers stay fresh.
 */
import { readFileSync, existsSync } from 'node:fs';

const pkg = JSON.parse(readFileSync('./package.json', 'utf8'));
const SUMMARY = 'coverage/coverage-summary.json';

if (!existsSync(SUMMARY)) {
  console.error(
    `[measure-coverage] ${SUMMARY} not found. Run \`npm run test:coverage\` first ` +
    `(or \`npm run coverage:doc\`, which chains it).`,
  );
  process.exit(1);
}

const summary = JSON.parse(readFileSync(SUMMARY, 'utf8'));
const pct = (m) => (m && typeof m.pct === 'number' ? m.pct : 0);

// Per-file rows: everything under src/, path relativized, sorted. `total` is
// pulled out separately for the headline + the leading "(all files)" row.
const rows = Object.entries(summary)
  .filter(([key]) => key !== 'total')
  .map(([key, m]) => {
    const i = key.lastIndexOf('/src/');
    const file = i === -1 ? key : `src/${key.slice(i + 5)}`;
    return { file, stmts: pct(m.statements), branch: pct(m.branches), funcs: pct(m.functions), lines: pct(m.lines) };
  })
  .filter((r) => r.file.startsWith('src/'))
  .sort((a, b) => a.file.localeCompare(b.file));

const total = summary.total ?? {};
const totalRow = {
  file: '(all files)',
  stmts: pct(total.statements),
  branch: pct(total.branches),
  funcs: pct(total.functions),
  lines: pct(total.lines),
};

const f1 = (n) => n.toFixed(1);

if (process.argv.includes('--json')) {
  console.log(JSON.stringify({ total: totalRow, files: rows }, null, 2));
} else if (process.argv.includes('--md')) {
  const mdRow = (r, bold) => {
    const name = bold ? `**${r.file}**` : `\`${r.file}\``;
    return `| ${name} | ${f1(r.stmts)} | ${f1(r.branch)} | ${f1(r.funcs)} | ${f1(r.lines)} |`;
  };
  console.log(`<!-- GENERATED by \`npm run coverage:doc\` (scripts/measure-coverage.mjs) — do not edit by hand. -->
# Coverage — vapor-chamber v${pkg.version}

Per-file coverage from the vitest v8 run (\`npm run test:coverage\`), regenerated by
\`npm run coverage:doc\`. Percentages are **statements · branches · functions · lines**.
Enforcement floors live in \`vitest.config.ts\`; the files that config \`exclude\`s
(re-export barrels, IIFE builders, \`vite-hmr\`, \`testing\`, …) are omitted here by design.

**Overall: ${f1(totalRow.stmts)}% statements · ${f1(totalRow.branch)}% branches · ${f1(totalRow.funcs)}% functions · ${f1(totalRow.lines)}% lines.**

| file | % stmt | % branch | % funcs | % lines |
|---|--:|--:|--:|--:|
${mdRow(totalRow, true)}
${rows.map((r) => mdRow(r, false)).join('\n')}`);
} else {
  const pad = (s, n) => String(s).padEnd(n);
  const padl = (s, n) => String(s).padStart(n);
  const line = (a, b, c, d, e) => `${pad(a, 26)}${padl(b, 8)}${padl(c, 9)}${padl(d, 8)}${padl(e, 8)}`;
  console.log(line('file', '% stmt', '% branch', '% func', '% line'));
  console.log('-'.repeat(59));
  console.log(line(totalRow.file, f1(totalRow.stmts), f1(totalRow.branch), f1(totalRow.funcs), f1(totalRow.lines)));
  for (const r of rows) console.log(line(r.file, f1(r.stmts), f1(r.branch), f1(r.funcs), f1(r.lines)));
}
